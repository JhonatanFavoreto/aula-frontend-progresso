// 1. Dobrar um número
const dobrar = n => n * 2;
console.log(dobrar(2));

// 2. Criar uma saudação
const saudar = nome => `Olá, ${nome}!`;
console.log(saudar('Jhonatan'));

// 3. Somar dois valores
const somar = (a, b) => a + b;
console.log(somar(3, 5));

// 4. Verificar se é par
const ehPar = n => n % 2 === 0;
console.log(ehPar(4));

// 5. Converter Celsius para Fahrenheit
const converter = celsius => (celsius * 9/5) + 32;
console.log(converter(100));